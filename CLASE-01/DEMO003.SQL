CREATE OR REPLACE FUNCTION SCOTT.FN_MAYOR
( N1 NUMBER, N2 NUMBER, N3 NUMBER )
RETURN NUMBER
IS
  MAYOR NUMBER := 0;
BEGIN
  IF( N1 > MAYOR ) THEN
    MAYOR := N1;
  END IF;
  IF( N2 > MAYOR ) THEN
    MAYOR := N2;
  END IF;
  IF( N3 > MAYOR ) THEN
    MAYOR := N3;
  END IF;
  RETURN MAYOR;
END;
/

SELECT SCOTT.FN_MAYOR( 12, 67, 30 ) FROM DUAL;
SELECT SCOTT.FN_MAYOR( -12, -67, -30 ) FROM DUAL;

