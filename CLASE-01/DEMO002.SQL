CREATE OR REPLACE PROCEDURE SCOTT.GET_EMPLEADO
( P_EMPNO NUMBER )
IS
  V_ENAME VARCHAR(20);
BEGIN
  SELECT ENAME INTO V_ENAME
  FROM SCOTT.EMP WHERE EMPNO = P_EMPNO;
  DBMS_OUTPUT.PUT_LINE( 'ENAME: ' || V_ENAME );
END;
/

CALL SCOTT.GET_EMPLEADO( 7499 );


CREATE OR REPLACE FUNCTION SCOTT.FN_EMPLEADO
( P_EMPNO NUMBER ) RETURN VARCHAR2
IS
  V_ENAME VARCHAR2(30);
BEGIN
  SELECT ENAME INTO V_ENAME
  FROM SCOTT.EMP WHERE EMPNO = P_EMPNO;
  RETURN V_ENAME;
END;
/

SELECT SCOTT.FN_EMPLEADO( 7499 ) FROM DUAL;


CREATE OR REPLACE FUNCTION SCOTT.FN_EMPLEADO
( P_EMPNO SCOTT.EMP.EMPNO%TYPE ) 
RETURN SCOTT.EMP.ENAME%TYPE
IS
  V_ENAME SCOTT.EMP.ENAME%TYPE;
BEGIN
  SELECT ENAME INTO V_ENAME
  FROM SCOTT.EMP WHERE EMPNO = P_EMPNO;
  RETURN V_ENAME;
END;
/


SELECT SCOTT.FN_EMPLEADO( 7499 ) FROM DUAL;


CREATE OR REPLACE FUNCTION RECURSOS.FN_SUELDO_TOTAL(
P_SUELDO   RECURSOS.EMPLEADO.SUELDO%TYPE,
P_COMISION RECURSOS.EMPLEADO.COMISION%TYPE
) RETURN RECURSOS.EMPLEADO.SUELDO%TYPE
IS
BEGIN
  RETURN ( P_SUELDO + NVL(P_COMISION,0) );
END;
/

SELECT 
  E.IDEMPLEADO, E.APELLIDO, E.NOMBRE,
  E.SUELDO, E.COMISION,
  RECURSOS.FN_SUELDO_TOTAL(E.SUELDO, E.COMISION) TOTAL
FROM RECURSOS.EMPLEADO E;




