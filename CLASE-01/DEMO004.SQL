CREATE OR REPLACE FUNCTION SCOTT.FN_FACTORIAL
( N NUMBER ) RETURN NUMBER
IS
  F NUMBER := 1;
  CONT NUMBER := N;
BEGIN
  LOOP
    F := F * CONT;
    CONT := CONT - 1;
    EXIT WHEN ( CONT = 0 );
  END LOOP;
  RETURN F;
END;
/

SELECT SCOTT.FN_FACTORIAL(5) FROM DUAL;

SELECT SCOTT.FN_FACTORIAL(0) FROM DUAL;


CREATE OR REPLACE FUNCTION SCOTT.FN_FACTORIAL2
( N NUMBER ) RETURN NUMBER
IS
  F NUMBER := 1;
  CONT NUMBER := N;
BEGIN
  WHILE ( CONT > 0 ) LOOP
    F := F * CONT;
    CONT := CONT - 1;
  END LOOP;
  RETURN F;
END;
/

SELECT SCOTT.FN_FACTORIAL2(5) FROM DUAL;

SELECT SCOTT.FN_FACTORIAL2(0) FROM DUAL;

SELECT SCOTT.FN_FACTORIAL2(-1) FROM DUAL;
