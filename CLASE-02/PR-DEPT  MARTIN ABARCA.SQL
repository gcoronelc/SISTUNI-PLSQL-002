CREATE OR REPLACE PROCEDURE SCOTT.PR_DEP_ROW(COD SCOTT.DEPT.DEPTNO%TYPE)
IS
REG SCOTT.DEPT%rowtype;

BEGIN
SELECT * INTO REG
  FROM SCOTT.DEPT
  WHERE DEPTNO=COD;
  DBMS_OUTPUT.PUT_LINE('CÓDIGO: ' ||REG.DEPTNO);
   DBMS_OUTPUT.PUT_LINE('NOMBRE: ' ||REG.DNAME);
    DBMS_OUTPUT.PUT_LINE('LOCALIZACION: ' ||REG.LOC);
END;
CALL  SCOTT.PR_DEP_ROW(10);