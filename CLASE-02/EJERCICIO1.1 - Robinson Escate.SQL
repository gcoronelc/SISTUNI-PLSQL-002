create or replace
FUNCTION FN_VAL_EMP(
P_EMP_ID RECURSOS.EMPLEADO.IDEMPLEADO%TYPE)
RETURN VARCHAR2
IS
V_MSG VARCHAR2(100);
CONT NUMBER;
BEGIN 
	SELECT COUNT(1) INTO CONT
	FROM EMPLEADO WHERE IDEMPLEADO = P_EMP_ID;
	IF ( CONT = 0 )  THEN
		V_MSG := (TO_CHAR(P_EMP_ID) || ' -> ' ||' EMPLEADO NO VALIDO');
	ELSE
		V_MSG := (TO_CHAR(P_EMP_ID) || ' -> ' ||' EMPLEADO EXITE');
	END IF;
	RETURN V_MSG;
END;
