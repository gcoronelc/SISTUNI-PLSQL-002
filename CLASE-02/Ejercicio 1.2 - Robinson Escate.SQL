CREATE OR REPLACE PROCEDURE recursos.PR_DEPTO2(
P_DEPTO_ID number)
IS
V_NOMBRE VARCHAR(200);
V_CONT NUMBER;
V_SUELDO NUMBER;
BEGIN 
 SELECT DPT.NOMBRE, COUNT(EMP.IDDEPARTAMENTO),SUM(EMP.SUELDO)
 INTO V_NOMBRE, V_CONT, V_SUELDO
  FROM EMPLEADO EMP
  INNER JOIN DEPARTAMENTO DPT ON DPT.IDDEPARTAMENTO=EMP.IDDEPARTAMENTO
  WHERE DPT.IDDEPARTAMENTO = P_DEPTO_ID
  GROUP BY  DPT.NOMBRE; 
  IF (V_CONT=0) THEN
    DBMS_OUTPUT.PUT_LINE('ÁREA NO VALIDA');
    ELSE
      DBMS_OUTPUT.PUT_LINE('Departamento: ' || V_NOMBRE ||' -> Empleados por Dpto: ' || V_CONT ||' -> Sueldo por Dpto: ' || V_SUELDO);
    END IF;
END;

call recursos.PR_DEPTO2 (104);