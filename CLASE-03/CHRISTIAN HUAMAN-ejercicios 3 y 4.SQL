Desarrollar un procedimiento que permita
registrar un nuevo empleado en esquema VENTAS.*/
--VER TABLA
select * from VENTAS.EMPLEADO;
----
CREATE OR REPLACE PROCEDURE VENTAS.REGI_EMPLE(
 COD VENTAS.EMPLEADO.IDEMP%TYPE,
 NOM VENTAS.EMPLEADO.NOMBRE%TYPE,
 APE VENTAS.EMPLEADO.APELLIDO%TYPE,
 EMA VENTAS.EMPLEADO.EMAIL%TYPE,
 TEL VENTAS.EMPLEADO.TELEFONO%TYPE
)
IS
BEGIN
 INSERT INTO VENTAS.EMPLEADO
 (IDEMP,NOMBRE,APELLIDO,EMAIL,TELEFONO)
 VALUES(COD,NOM,APE,EMA,TEL);
END;
/
--REGISTRAR EMPLEADO
CALL VENTAS.REGI_EMPLE('1005','CHRISTIAN MICHAEL','Huamán Ca','hchristian6@live.com','928536863'); 
