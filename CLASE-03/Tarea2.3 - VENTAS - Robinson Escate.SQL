CREATE OR REPLACE PROCEDURE PR_NEW_EMP
(P_NOMBRE ventas.EMPLEADO.NOMBRE%TYPE,
P_APELLIDO ventas.EMPLEADO.APELLIDO%TYPE,
P_EMAIL ventas.EMPLEADO.EMAIL%TYPE,
P_TELEFONO ventas.EMPLEADO.TELEFONO%TYPE)
IS
  V_COD NUMBER;
BEGIN
  SELECT MAX(IDEMP)+1 INTO V_COD FROM VENTAS.EMPLEADO;
  INSERT INTO ventas.EMPLEADO VALUES (V_COD,P_NOMBRE,P_APELLIDO,P_EMAIL,P_TELEFONO);
  COMMIT;
END;

